package day03_EscapeSequences;

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("Python\nJava\nC#");
        System.out.println("");
        System.out.println("\tHello everyone? \nhow are you all today? \nToday we will learn Escape Sequences.");
        System.out.println("\tThis is a tab function");

        System.out.println("-------------------------");

        System.out.println("/ \\ \\");

        System.out.println("\"Java is the best language\""); // in order to make double quote

    }


}
