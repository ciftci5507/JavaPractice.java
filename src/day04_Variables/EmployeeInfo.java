        String name = "Johannes";
        int age = 24;
        char gender = 'M';
        String company = "Apple Inc";
        String jobTitle = "SDET";
        double yearsOfExperience = 6.0;
        int salary = 4500;
        boolean isFullTime = true;
        boolean isMarried = false;
        String employeeId = "A101";
