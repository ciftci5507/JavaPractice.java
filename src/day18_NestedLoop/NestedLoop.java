package day18_NestedLoop;

public class NestedLoop {
    public static void main(String[] args) {

        int count =0;
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 34; j++) {
                System.out.println("Wooden Spoon");
                count++;
            }
        }

        System.out.println();

        System.out.println(count);



    }
}
