package day15_String_ForLoop;

public class ForLoopPractices {
    public static void main(String[] args) {

        //15 16 17 18 ----- 45

        for (int i = 15; i <= 45; i++) {
            System.out.print(i + " ");
        }

        for (int t = 1; t <= 2; t++) {
            System.out.print(t);
        }

        System.out.println();

        System.out.println("Hello World!");


        int a = 1;
        System.out.println(a++);


        System.out.println("------------------------");

        for (int i = 1; i <= 55; i++) {
            if (i % 2 == 0) {
                System.out.print(i+ " ");
            }

            System.out.println();




        }
    }
}